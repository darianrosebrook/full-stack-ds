/**
 * React lowering for the shared layout primitive (Stack).
 *
 * Generates `primitives/stack.tsx` + `primitives/stack.css` from the
 * framework-neutral `PrimitiveIR`. All layout facts (class prefix, display
 * map, axis map, token-driven gap) come from the IR; this emitter only knows
 * how to render them as React + CSS. There is no Stack-specific branching.
 */
import type { PrimitiveIR } from "../../primitive-contract.js";

/**
 * Banner marking the primitive as whole-file generated output. Unlike
 * components, the primitive is not preserve-merged — it is regenerated
 * byte-for-byte from the contract, so a plain leading comment is enough.
 */
const BANNER =
  "// @generated by ds-codegen from primitives/Stack.primitive.json — do not edit by hand.";

/** Lower-case file base for the primitive module (e.g. Stack -> "stack"). */
function moduleBase(ir: PrimitiveIR): string {
  return ir.name.toLowerCase();
}

export function generateReactStackPrimitiveSource(ir: PrimitiveIR): string {
  const prefix = ir.layout.classPrefix;
  const base = moduleBase(ir);
  const variants = Object.keys(ir.layout.axisByVariant);
  const modes = Object.keys(ir.layout.displayByMode);
  const defaultVariant =
    (ir.props.find((p) => p.kind === "layoutVariant")?.default as string) ??
    variants[0];
  const defaultMode =
    (ir.props.find((p) => p.kind === "layoutMode")?.default as string) ??
    modes[0];

  const variantUnion = variants.map((v) => `"${v}"`).join(" | ");
  const modeUnion = modes.map((m) => `"${m}"`).join(" | ");

  return `${BANNER}
import {
  createElement,
  type ElementType,
  type ComponentPropsWithoutRef,
  type ReactNode,
  type Ref,
} from "react";
import "./${base}.css";

type StackVariant = ${variantUnion};
type StackLayout = ${modeUnion};

type StackOwnProps<T extends ElementType = "div"> = {
  as?: T;
  variant?: StackVariant;
  layout?: StackLayout;
  ref?: Ref<Element>;
  children?: ReactNode;
};

export type StackProps<T extends ElementType = "div"> = StackOwnProps<T> &
  Omit<ComponentPropsWithoutRef<T>, keyof StackOwnProps<T>>;

export function ${ir.name}<T extends ElementType = "div">({
  as,
  variant = "${defaultVariant}",
  layout = "${defaultMode}",
  className,
  ref,
  ...rest
}: StackProps<T>) {
  const Component = as || "div";
  const classNames = ["${prefix}", \`${prefix}--layout-\${layout}\`, \`${prefix}--\${variant}\`, className]
    .filter(Boolean)
    .join(" ");

  return createElement(Component, { ref, className: classNames, ...rest });
}
`;
}

export function generateReactStackPrimitiveCss(ir: PrimitiveIR): string {
  const prefix = ir.layout.classPrefix;
  const blocks: string[] = [];

  // Base block: box model + token-driven gap.
  const baseDecls = ["box-sizing: border-box;"];
  if (ir.layout.gap) {
    baseDecls.push(`gap: var(${ir.layout.gap.cssVar});`);
  }
  blocks.push(`.${prefix} {\n  ${baseDecls.join("\n  ")}\n}`);

  // display per layout mode (null => host keeps its own display, no rule).
  for (const [mode, display] of Object.entries(ir.layout.displayByMode)) {
    if (display === null) continue;
    blocks.push(`.${prefix}--layout-${mode} {\n  display: ${display};\n}`);
  }

  // flex-direction per variant.
  for (const [variant, axis] of Object.entries(ir.layout.axisByVariant)) {
    blocks.push(`.${prefix}--${variant} {\n  flex-direction: ${axis};\n}`);
  }

  return `/* @generated by ds-codegen from primitives/Stack.primitive.json — do not edit by hand. */\n${blocks.join("\n\n")}\n`;
}
