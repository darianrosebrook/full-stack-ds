/**
 * Vue lowering for the shared layout primitive (Stack).
 *
 * Generates `primitives/Stack.vue` from the framework-neutral `PrimitiveIR`.
 * All layout facts (class prefix, display map, axis map, token-driven gap)
 * come from the IR; this emitter only knows how to render them as a Vue SFC.
 * There is no Stack-specific branching.
 */
import type { PrimitiveIR } from "../../primitive-contract.js";

/**
 * Banner marking the primitive as whole-file generated output. Unlike
 * components, the primitive is not preserve-merged — it is regenerated
 * byte-for-byte from the contract, so a plain leading comment is enough.
 */
const BANNER =
  "<!-- @generated by ds-codegen from primitives/Stack.primitive.json — do not edit by hand. -->";

export function generateVueStackPrimitiveSource(ir: PrimitiveIR): string {
  const prefix = ir.layout.classPrefix;
  const variants = Object.keys(ir.layout.axisByVariant);
  const modes = Object.keys(ir.layout.displayByMode);
  const defaultVariant =
    (ir.props.find((p) => p.kind === "layoutVariant")?.default as string) ??
    variants[0];
  const defaultMode =
    (ir.props.find((p) => p.kind === "layoutMode")?.default as string) ??
    modes[0];

  const variantUnion = variants.map((v) => `"${v}"`).join(" | ");
  const modeUnion = modes.map((m) => `"${m}"`).join(" | ");

  const styleBlocks: string[] = [];
  const baseDecls = ["box-sizing: border-box;"];
  if (ir.layout.gap) {
    baseDecls.push(`gap: var(${ir.layout.gap.cssVar});`);
  }
  styleBlocks.push(`.${prefix} {\n  ${baseDecls.join("\n  ")}\n}`);

  for (const [mode, display] of Object.entries(ir.layout.displayByMode)) {
    if (display === null) continue;
    styleBlocks.push(`.${prefix}--layout-${mode} {\n  display: ${display};\n}`);
  }

  for (const [variant, axis] of Object.entries(ir.layout.axisByVariant)) {
    styleBlocks.push(`.${prefix}--${variant} {\n  flex-direction: ${axis};\n}`);
  }

  return `${BANNER}
<script setup lang="ts">
import { computed } from "vue";

export interface ${ir.name}Props {
  as?: string;
  variant?: ${variantUnion};
  layout?: ${modeUnion};
  className?: string;
}

const props = withDefaults(defineProps<${ir.name}Props>(), {
  as: "div",
  variant: "${defaultVariant}",
  layout: "${defaultMode}",
});

const classNames = computed(() =>
  ["${prefix}", \`${prefix}--layout-\${props.layout}\`, \`${prefix}--\${props.variant}\`, props.className]
    .filter(Boolean)
    .join(" "),
);
</script>

<template>
  <component :is="props.as" :class="classNames">
    <slot />
  </component>
</template>

<style scoped>
${styleBlocks.join("\n")}
</style>
`;
}
