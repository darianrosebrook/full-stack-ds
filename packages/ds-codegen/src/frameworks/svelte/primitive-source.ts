/**
 * Svelte 5 lowering for the shared layout primitive (Stack).
 *
 * Generates `primitives/Stack.svelte` from the framework-neutral
 * `PrimitiveIR`. All layout facts (class prefix, display map, axis map,
 * token-driven gap) come from the IR; this emitter only knows how to render
 * them as a Svelte 5 (runes) single-file component. There is no
 * Stack-specific branching.
 *
 * Svelte's vendor-prefixed block name (`FSDS-stack`) is derived by prefixing
 * the contract's `layout.classPrefix` ("stack") with the `FSDS-` vendor
 * prefix — the prefix itself is not hardcoded as a whole string.
 */
import type { PrimitiveIR } from "../../primitive-contract.js";

/**
 * Banner marking the primitive as whole-file generated output. Unlike
 * components, the primitive is not preserve-merged — it is regenerated
 * byte-for-byte from the contract, so a plain leading comment is enough.
 */
const BANNER =
  "<!-- @generated by ds-codegen from primitives/Stack.primitive.json — do not edit by hand. -->";

/** Svelte's vendor prefix for generated layout primitive classes. */
const VENDOR_PREFIX = "FSDS-";

export function generateSvelteStackPrimitiveSource(ir: PrimitiveIR): string {
  const block = `${VENDOR_PREFIX}${ir.layout.classPrefix}`;
  const variants = Object.keys(ir.layout.axisByVariant);
  const modes = Object.keys(ir.layout.displayByMode);
  const defaultVariant =
    (ir.props.find((p) => p.kind === "layoutVariant")?.default as string) ??
    variants[0];
  const defaultMode =
    (ir.props.find((p) => p.kind === "layoutMode")?.default as string) ??
    modes[0];

  const variantUnion = variants.map((v) => `'${v}'`).join(" | ");
  const modeUnion = modes.map((m) => `'${m}'`).join(" | ");

  const styleBlocks: string[] = [];
  const baseDecls = ["box-sizing: border-box;"];
  if (ir.layout.gap) {
    baseDecls.push(`gap: var(${ir.layout.gap.cssVar});`);
  }
  styleBlocks.push(`  .${block} {\n    ${baseDecls.join("\n    ")}\n  }`);

  for (const [mode, display] of Object.entries(ir.layout.displayByMode)) {
    if (display === null) continue;
    styleBlocks.push(`  .${block}--layout-${mode} {\n    display: ${display};\n  }`);
  }

  for (const [variant, axis] of Object.entries(ir.layout.axisByVariant)) {
    styleBlocks.push(`  .${block}--${variant} {\n    flex-direction: ${axis};\n  }`);
  }

  return `${BANNER}
<script lang="ts">
  interface Props {
    variant?: ${variantUnion};
    layout?: ${modeUnion};
    as?: keyof HTMLElementTagNameMap;
    role?: string;
    class?: string;
    'data-testid'?: string;
    children?: import('svelte').Snippet;
  }

  let {
    variant = '${defaultVariant}',
    layout = '${defaultMode}',
    as = 'div',
    role,
    class: className,
    'data-testid': dataTestid,
    children,
  }: Props = $props();

  const classes = $derived(
    ['${block}', \`${block}--layout-\${layout}\`, \`${block}--\${variant}\`, className].filter(Boolean).join(' ')
  );
</script>

<svelte:element this={as} class={classes} {role} data-testid={dataTestid}>
  {@render children?.()}
</svelte:element>

<style>
${styleBlocks.join("\n\n")}
</style>
`;
}
